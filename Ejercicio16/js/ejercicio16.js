document.write("<h1>"+"While"+"</h1>")
let x = 100;
while (x >= 1) {
    document.write(x+", ")
    x--
}

document.write("<h1>"+"Do While"+"</h1>")
let y = 100;
do{
    document.write(y+", ")
    y--
} while (y >= 1) 


document.write("<h1>"+"For"+"</h1>")
for (let z=100; z>=1; z--) {
    document.write(z+", ")
} 